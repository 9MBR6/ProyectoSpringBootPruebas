package com.dtgz.motormaint.model.enums;

/**
 * Enumeración que representa los diferentes tipos de motor que puede tener una motocicleta.
 */
public enum TipoMotor {
    /** Motor de dos tiempos. */
    DOS_TIEMPOS,
    /** Motor de cuatro tiempos. */
    CUATRO_TIEMPOS,
    /** Motor eléctrico. */
    ELECTRICO,
    /** Motor híbrido. */
    HIBRIDO,
    /** Otro tipo de motor. */
    OTRO
}